public class AssignmentOPerator {
    public static void main(String[] args) {
        int p = 4;
        int q;
        q = p;
        System.out.println(q);
        System.out.println(p+=q);
        System.out.println(p-=q);
        System.out.println(q*=q);
        System.out.println(q/=q);

    }
}
